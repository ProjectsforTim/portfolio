-- Safety: drop tables if they already exist (order matters due to FKs)
DROP TABLE IF EXISTS maintenance_logs CASCADE;
DROP TABLE IF EXISTS payments CASCADE;
DROP TABLE IF EXISTS customers CASCADE;
DROP TABLE IF EXISTS drones CASCADE;

BEGIN;

-- =========================
-- Tables
-- =========================

CREATE TABLE drones (
    drone_id SERIAL PRIMARY KEY,
    model_name VARCHAR(100) NOT NULL,
    manufacturer VARCHAR(100) NOT NULL,
    weight_kg DECIMAL(5,2),
    max_range_km DECIMAL(5,2),
    price_aud DECIMAL(10,2),
    in_service BOOLEAN DEFAULT TRUE
);

CREATE TABLE customers (
    customer_id SERIAL PRIMARY KEY,
    full_name VARCHAR(150) NOT NULL,
    email VARCHAR(120) UNIQUE NOT NULL,
    phone VARCHAR(20),
    address TEXT,
    registration_date DATE DEFAULT CURRENT_DATE
);

CREATE TABLE payments (
    payment_id SERIAL PRIMARY KEY,
    customer_id INT NOT NULL REFERENCES customers(customer_id),
    drone_id INT NOT NULL REFERENCES drones(drone_id),
    amount_aud DECIMAL(10,2) NOT NULL CHECK (amount_aud >= 0),
    payment_method VARCHAR(50) NOT NULL CHECK (payment_method IN ('Visa','Mastercard','Amex','PayPal','Bank Transfer','Zip Pay')),
    payment_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    status VARCHAR(20) NOT NULL DEFAULT 'Completed' CHECK (status IN ('Completed','Pending','Failed','Refunded'))
);

CREATE INDEX idx_payments_customer ON payments(customer_id);
CREATE INDEX idx_payments_drone ON payments(drone_id);
CREATE INDEX idx_payments_date ON payments(payment_date);

CREATE TABLE maintenance_logs (
    log_id SERIAL PRIMARY KEY,
    drone_id INT NOT NULL REFERENCES drones(drone_id),
    maintenance_date DATE DEFAULT CURRENT_DATE,
    issue_reported TEXT,
    action_taken TEXT,
    technician_name VARCHAR(100)
);

CREATE INDEX idx_maint_drone ON maintenance_logs(drone_id);

